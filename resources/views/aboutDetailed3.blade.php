<h1 class="entry-title">Suzanne Anderson, MSS</h1>
<div class="entry-content">
<p style="text-align: justify;">Suzanne holds a Masters Degree in Social Sciences (Social Work) from the National
University of Singapore and is registered with the Singapore Association of Social
Workers. Formerly with SACAC Counselling, Suzanne’s experience includes working with
issues related to trauma, grief and loss, adjusting to change and transition, facing life
threatening illnesses, new life roles, and trauma related to accident, natural disaster, rape,
sexual assault, family violence from a strengths and resilience perspective. Additionally,
her experience has included working with adolescents and youth and their parents on
issues related to behaviour, communication and parenting concerns.</p>
<h4>Crisis Response</h4>
<p style="text-align: justify;">
Suzanne is a Certified Crisis Responder and Trainer with the National Organization for
Victim Assistance in the United States. Suzanne provides crisis response services to
individuals and communities impacted by crimes, natural disasters and industrial
accidents. Suzanne’s experience in crisis response spans more than 20 years. Services
include counselling, consultation, and on-site response.</p>

<h4>Training</h4>
<p style="text-align: justify;">
Suzanne has been a trainer in multi-national and local corporations, educational
institutions as well as having been a lecturer/tutor for Monash and Edith Cowan
Universities’ off-shore social work & social science courses. She has provided crisis
response, cross-cultural, social work and parenting training in Singapore and the region
including China, Thailand, Indonesia, India, Vietnam, Burma/Myanmar, Nepal as well as
the United States, Canada and Yugoslavia.</p>
<p style="text-align: justify;"> Suzanne is an American who has resided in Singapore since 1999. She is currently
pursuing a Doctorate of Professional Studies from the University of Southern Queensland,
Australia.</p>
